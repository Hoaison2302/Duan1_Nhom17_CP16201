package com.example.duan1_nhom17_cp16201;

public class Diem {
    public int sothutu;
    public String tennguoichoi;
    public int diemtong;
    public String ngay;

    public int getSothutu() {
        return sothutu;
    }

    public void setSothutu(int sothutu) {
        this.sothutu = sothutu;
    }

    public String getTennguoichoi() {
        return tennguoichoi;
    }

    public void setTennguoichoi(String tennguoichoi) {
        this.tennguoichoi = tennguoichoi;
    }

    public int getDiemtong() {
        return diemtong;
    }

    public void setDiemtong(int diemtong) {
        this.diemtong = diemtong;
    }

    public String getNgay() {
        return ngay;
    }

    public void setNgay(String ngay) {
        this.ngay = ngay;
    }
}
